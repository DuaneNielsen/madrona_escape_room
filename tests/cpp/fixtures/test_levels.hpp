class TestLevelHelper {
public:
    static MER_CompiledLevel GetEmbeddedTestLevel() {
        MER_CompiledLevel level = {};  // Zero-initialize
        
        // The embedded data was from the old format, just create a simple test level
        level.width = 5;
        level.height = 5; 
        level.scale = 2.0f;
        level.num_tiles = 16;  // Border walls only
        level.max_entities = 20;
        
        // Create a 5x5 room with walls on borders
        int tile_idx = 0;
        
        // Top and bottom walls
        for (int x = 0; x < 5; x++) {
            // Top wall
            level.object_ids[tile_idx] = 1;  // Wall type
            level.tile_x[tile_idx] = x * level.scale - 5.0f;
            level.tile_y[tile_idx] = 0 * level.scale - 5.0f;
            tile_idx++;
            
            // Bottom wall
            level.object_ids[tile_idx] = 1;  // Wall type
            level.tile_x[tile_idx] = x * level.scale - 5.0f;
            level.tile_y[tile_idx] = 4 * level.scale - 5.0f;
            tile_idx++;
        }
        
        // Left and right walls (excluding corners already placed)
        for (int y = 1; y < 4; y++) {
            // Left wall
            level.object_ids[tile_idx] = 1;  // Wall type
            level.tile_x[tile_idx] = 0 * level.scale - 5.0f;
            level.tile_y[tile_idx] = y * level.scale - 5.0f;
            tile_idx++;
            
            // Right wall
            level.object_ids[tile_idx] = 1;  // Wall type
            level.tile_x[tile_idx] = 4 * level.scale - 5.0f;
            level.tile_y[tile_idx] = y * level.scale - 5.0f;
            tile_idx++;
        }
        
        level.num_tiles = tile_idx;
        
        // Initialize render-only flags for test tiles (all physics entities by default)
        for (int i = 0; i < level.num_tiles; i++) {
            level.tile_render_only[i] = false;  // Default: all physics entities
        }
        
        // Initialize entity type values for test tiles
        for (int i = 0; i < level.num_tiles; i++) {
            // All walls in this test level use object_id = 1, which corresponds to WALL
            // Map to EntityType::Wall (value 2)
            if (level.object_ids[i] == 1) {  // WALL
                level.tile_entity_type[i] = 2;  // EntityType::Wall
            } else {
                level.tile_entity_type[i] = 0;  // EntityType::None (default)
            }
        }
        
        // Initialize rest as None (default)
        for (int i = level.num_tiles; i < MER_MAX_TILES; i++) {
            level.tile_entity_type[i] = 0;  // EntityType::None
        }
        
        return level;
    }
    
    static std::vector<MER_CompiledLevel> CreateTestLevels(int32_t count) {
        std::vector<MER_CompiledLevel> levels(count);
        MER_CompiledLevel test_level = GetEmbeddedTestLevel();
        for (int32_t i = 0; i < count; i++) {
            levels[i] = test_level;
        }
        return levels;
    }
    
    static MER_CompiledLevel LoadLevelFromFile(const std::string& filepath) {
        MER_CompiledLevel level = {};
        std::ifstream file(filepath, std::ios::binary);
        if (file) {
            file.read(reinterpret_cast<char*>(&level), sizeof(MER_CompiledLevel));
        }
        return level;
    }
};