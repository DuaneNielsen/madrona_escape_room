static unsigned int embedded_test_level_len = 3092;

int main() {
    printf("Testing C wrapper GPU manager...\n");
    
    MER_ManagerConfig config = {};
    config.exec_mode = MER_EXEC_MODE_CUDA;
    config.gpu_id = 0;
    config.num_worlds = 4;
    config.rand_seed = 42;
    config.auto_reset = true;
    config.enable_batch_renderer = false;
    
    // Load test level from embedded binary data
    MER_CompiledLevel test_level;
    if (embedded_test_level_len != sizeof(MER_CompiledLevel)) {
        fprintf(stderr, "Error: Embedded level data size mismatch. Expected %zu, got %u\n", 
                sizeof(MER_CompiledLevel), embedded_test_level_len);
        return 1;
    }
    
    memcpy(&test_level, embedded_test_level, sizeof(MER_CompiledLevel));
    printf("Loaded embedded test level: %dx%d grid, %d tiles\n", 
           test_level.width, test_level.height, test_level.num_tiles);
    
    // Create array with same level for all worlds
    MER_CompiledLevel test_levels[4];
    for (int i = 0; i < 4; i++) {
        test_levels[i] = test_level;
    }
    
    MER_ManagerHandle handle = nullptr;
    printf("Creating GPU manager...\n");
    MER_Result result = mer_create_manager(&handle, &config, test_levels, 4);  // Provide test levels
    
    if (result != MER_SUCCESS) {
        fprintf(stderr, "Failed to create GPU manager: %s\n", mer_result_to_string(result));
        return 1;
    }
    
    printf("GPU manager created successfully!\n");
    
    // Test tensor access
    MER_Tensor action_tensor;
    result = mer_get_action_tensor(handle, &action_tensor);
    if (result != MER_SUCCESS) {
        fprintf(stderr, "Failed to get action tensor: %s\n", mer_result_to_string(result));
    } else {
        printf("Got action tensor - GPU ID: %d, shape: [", action_tensor.gpu_id);
        for (int i = 0; i < action_tensor.num_dimensions; i++) {
            if (i > 0) printf(", ");
            printf("%lld", (long long)action_tensor.dimensions[i]);
        }
        printf("]\n");
    }
    
    // Clean up
    mer_destroy_manager(handle);
    printf("GPU manager destroyed\n");
    
    printf("\nGPU test passed!\n");
    return 0;
}